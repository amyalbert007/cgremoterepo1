package cg.JavaStringMethods;

public class IsEmpty {
	
	
	
	
	
	
	
	public static void main(String args[]){  
		String s1="";  
		String s2="javatpoint";  
		  
		System.out.println(s1.isEmpty());  
		System.out.println(s2.isEmpty());
		
		
		
		
		
		
		
		
		String ss1="";    
        String ss2="Javatpoint";             
        // Either length is zero or isEmpty is true  
        if(ss1.length()==0 || ss1.isEmpty())  
            System.out.println("String ss1 is empty");  
        else System.out.println("ss1");        
        if(ss2.length()==0 || ss2.isEmpty())  
            System.out.println("String ss2 is empty");  
        else System.out.println(ss2);
		
		
		
		
		
		
		
		
		
		}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

}
